package cn.chono.yopper.Service.Http.InviteType;

import cn.chono.yopper.Service.Http.ParameterBean;

/**
 * Created by zxb on 2015/11/22.
 */
public class InviteTypeBean extends ParameterBean {
    private  int InviteeId;
    private  int InviteType;
    private  boolean Confirm;

    public int getInviteeId() {
        return InviteeId;
    }

    public void setInviteeId(int inviteeId) {
        InviteeId = inviteeId;
    }

    public int getInviteType() {
        return InviteType;
    }

    public void setInviteType(int inviteType) {
        InviteType = inviteType;
    }

    public boolean isConfirm() {
        return Confirm;
    }

    public void setConfirm(boolean confirm) {
        Confirm = confirm;
    }
}
