package cn.chono.yopper.data;

import java.io.Serializable;
import java.util.List;

public class BubblingDto implements Serializable {

	private int rows;
	private int start;
	private int total;
	private List<BubblingList> list;
	
	

	public int getRows() {
		return rows;
	}

	public void setRows(int rows) {
		this.rows = rows;
	}

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public List<BubblingList> getList() {
		return list;
	}

	public void setList(List<BubblingList> list) {
		this.list = list;
	}

	

	



}
