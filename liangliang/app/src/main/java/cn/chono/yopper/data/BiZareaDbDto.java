package cn.chono.yopper.data;

import com.lidroid.xutils.db.annotation.Column;
import com.lidroid.xutils.db.annotation.Table;


@Table(name ="BiZareaDbDto")

public class BiZareaDbDto {

	@Column(column = "id")
	private int id;

	@Column(column = "name")
	private String name;

	@Column(column = "zareaStr")
	private String zareaStr;


	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getZareaStr() {
		return zareaStr;
	}

	public void setZareaStr(String zareaStr) {
		this.zareaStr = zareaStr;
	}

	public BiZareaDbDto( String name, String zareaStr) {
		super();
		this.name = name;
		this.zareaStr = zareaStr;

	}

	public BiZareaDbDto() {
		super();
	}

}
