package cn.chono.yopper.data;

import java.io.Serializable;

public class NearPlaceDto implements Serializable{

	
  
	// 是否她想去
	private boolean isTargetUserFavorite;

	private NearLoc loc;
	// 是否有优惠劵
	private boolean hasCoupon;
	// 距离
	private double distance;
	// 碰的数量
	private int bumpCount;

	public boolean isTargetUserFavorite() {
		return isTargetUserFavorite;
	}

	public void setTargetUserFavorite(boolean isTargetUserFavorite) {
		this.isTargetUserFavorite = isTargetUserFavorite;
	}

	public NearLoc getLoc() {
		return loc;
	}

	public void setLoc(NearLoc loc) {
		this.loc = loc;
	}

	public boolean isHasCoupon() {
		return hasCoupon;
	}

	public void setHasCoupon(boolean hasCoupon) {
		this.hasCoupon = hasCoupon;
	}

	public double getDistance() {
		return distance;
	}

	public void setDistance(double distance) {
		this.distance = distance;
	}

	public int getBumpCount() {
		return bumpCount;
	}

	public void setBumpCount(int bumpCount) {
		this.bumpCount = bumpCount;
	}

}
