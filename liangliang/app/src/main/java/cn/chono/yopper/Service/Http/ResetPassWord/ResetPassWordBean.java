package cn.chono.yopper.Service.Http.ResetPassWord;

import cn.chono.yopper.Service.Http.ParameterBean;

/**
 * Created by zxb on 2015/11/20.
 */
public class ResetPassWordBean extends ParameterBean {

    private String hashedNewPassword;
    private  String confirmHashedPassword;

    public String getHashedNewPassword() {
        return hashedNewPassword;
    }

    public void setHashedNewPassword(String hashedNewPassword) {
        this.hashedNewPassword = hashedNewPassword;
    }

    public String getConfirmHashedPassword() {
        return confirmHashedPassword;
    }

    public void setConfirmHashedPassword(String confirmHashedPassword) {
        this.confirmHashedPassword = confirmHashedPassword;
    }
}
