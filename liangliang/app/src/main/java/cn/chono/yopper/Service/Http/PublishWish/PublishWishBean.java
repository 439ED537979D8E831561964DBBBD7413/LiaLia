package cn.chono.yopper.Service.Http.PublishWish;

import cn.chono.yopper.Service.Http.ParameterBean;

/**
 * Created by zxb on 2015/11/21.
 */
public class PublishWishBean extends ParameterBean {


    private String content;
    private  double lat;
    private  double lng;
    private  int datingType;
    private  int locationId;

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLng() {
        return lng;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }

    public int getDatingType() {
        return datingType;
    }

    public void setDatingType(int datingType) {
        this.datingType = datingType;
    }

    public int getLocationId() {
        return locationId;
    }

    public void setLocationId(int locationId) {
        this.locationId = locationId;
    }
}
